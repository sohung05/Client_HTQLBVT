    /**
     * Tìm Gui_BanVe trong container (đệ quy)
     */
    private Gui_BanVe findGuiBanVe(Container container) {
        for (Component comp : container.getComponents()) {
            if (comp instanceof Gui_BanVe) {
                return (Gui_BanVe) comp;
            } else if (comp instanceof Container) {
                Gui_BanVe result = findGuiBanVe((Container) comp);
                if (result != null) {
                    return result;
                }
            }
        }
        return null;
    }
    
    /**
     * Tìm Gui_BanVe trong MainForm (dùng cho xử lý đơn treo)
     */
    private Gui_BanVe findGuiBanVeInMainForm(iuh.fit.gui.menu.form.MainForm mainForm) {
        try {
            // MainForm thường có cấu trúc: MainForm -> Body (JPanel) -> CurrentForm
            Component[] components = mainForm.getComponents();
            for (Component comp : components) {
                if (comp instanceof Container) {
                    Gui_BanVe result = findGuiBanVe((Container) comp);
                    if (result != null) {
                        return result;
                    }
                }
            }
        } catch (Exception e) {
            System.err.println("Lỗi khi tìm Gui_BanVe: " + e.getMessage());
        }
        return null;
    }
    
    /**
     * Lưu dữ liệu vào database
     * @return true nếu thành công
     */
    private boolean luuVaoDatabase(String maHoaDon, String cccd, String hoTen, String sdt, String email, 
                                     int soLuongVe, double tongTien, double khuyenMai) {
        try {
            IKhachHangService khachHangService = ClientContext.getKhachHangService();
            IHoaDonService hoaDonService = ClientContext.getHoaDonService();
            IVeService veService = ClientContext.getVeService();
            ILoaiVeService loaiVeService = ClientContext.getLoaiVeService();
            ILichTrinhService lichTrinhService = ClientContext.getLichTrinhService();

            // ===== 1. TẠO/LẤY KHÁCH HÀNG =====
            KhachHang kh = khachHangService.findByCCCD(cccd);
            
            if (kh == null) {
                // Tạo khách hàng mới
                kh = new KhachHang();
                kh.setMaKH("KH" + System.currentTimeMillis());
                kh.setCCCD(cccd);
                kh.setHoTen(hoTen);
                kh.setSDT(sdt);
                kh.setEmail(email);
                kh.setDoiTuong(null);
                
                if (!khachHangService.them(kh)) {
                    System.out.println("Lỗi: Không thể tạo khách hàng!");
                    return false;
                }
            }
            
            // ===== 2. TẠO HÓA ĐƠN =====
            HoaDon hoaDon = new HoaDon();
            hoaDon.setMaHoaDon(maHoaDon);
            
            // Lấy nhân viên từ session
            NhanVien nv = new NhanVien();
            nv.setMaNhanVien(iuh.fit.utils.SessionManager.getInstance().getMaNhanVienDangNhap());
            hoaDon.setNhanVien(nv);
            
            hoaDon.setKhachHang(kh);
            hoaDon.setNgayTao(LocalDateTime.now());
            hoaDon.setGioTao(LocalDateTime.now());
            hoaDon.setTongTien(tongTien - khuyenMai);
            hoaDon.setTrangThai(true);
            
            // ===== 3. LƯU VÉ VÀ CHI TIẾT HÓA ĐƠN =====
            if (donTreo != null) {
                List<DonTreoDat.ThongTinVeTam> danhSachVe = donTreo.getDanhSachVe();
                for (int i = 0; i < danhSachVe.size(); i++) {
                    DonTreoDat.ThongTinVeTam veTam = danhSachVe.get(i);
                    LoaiVe loaiVe = loaiVeService.findByTenLoaiVe(veTam.getDoiTuong());
                    if (loaiVe == null) return false;
                    
                    LichTrinh lichTrinhCuaVe = veTam.getLichTrinh();
                    if (lichTrinhCuaVe != null) {
                        lichTrinhCuaVe = lichTrinhService.findById(lichTrinhCuaVe.getMaLichTrinh());
                    }
                    
                    Ve ve = new Ve();
                    ve.setMaVe("V" + System.currentTimeMillis() + "_" + i);
                    ve.setLoaiVe(loaiVe);
                    ve.setThoiGianLenTau(lichTrinhCuaVe != null ? lichTrinhCuaVe.getGioKhoiHanh() : null);
                    ve.setGiaVe(veTam.getGiaVe());
                    ve.setKhachHang(kh);
                    ve.setChoNgoi(veTam.getChoNgoi());
                    ve.setLichTrinh(lichTrinhCuaVe);
                    ve.setTrangThai(true);
                    ve.setTenKhachHang(veTam.getHoTen());
                    ve.setSoCCCD(veTam.getSoGiayTo());
                    ve.setGaDi(veTam.getGaDi());
                    ve.setGaDen(veTam.getGaDen());
                    
                    // Lưu vé trước khi tạo chi tiết hóa đơn (RMI/Hibernate JPA)
                    veService.insert(ve);
                    
                    ChiTietHoaDon cthd = new ChiTietHoaDon();
                    cthd.setVe(ve);
                    cthd.setSoLuong(1);
                    cthd.setGiaVe(veTam.getGiaVe());
                    cthd.setMucGiam(veTam.getGiamGia());
                    
                    hoaDon.themChiTiet(cthd);
                }
            }
            
            boolean kq = hoaDonService.them(hoaDon);
            if (kq) {
                System.out.println("✅ Lưu database thành công! Mã hóa đơn: " + maHoaDon);
            }
            return kq;
            
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("❌ LỖI: " + e.getMessage());
            return false;
        }
    }

