    /**
     * Tự động in hóa đơn và vé (gọi sau khi thanh toán thành công)
     */
    public void autoInHoaDonVaVe() {
        if (maHoaDon == null || maHoaDon.isEmpty()) {
            System.err.println("❌ Không có mã hóa đơn để in!");
            return;
        }
        
        // Chạy trong background thread để không block UI
        new Thread(() -> {
            try {
                System.out.println("🚀 Bắt đầu tự động in hóa đơn và vé...");
                
                // Bước 1: In hóa đơn trước
                System.out.println("📋 Bước 1: In hóa đơn...");
                inHoaDon(maHoaDon);
                
                // Đợi 2 giây trước khi in vé
                System.out.println("⏳ Đợi 2 giây trước khi in vé...");
                Thread.sleep(2000);
                
                // Bước 2: In tất cả vé (mỗi vé cách nhau 2s)
                System.out.println("🎫 Bước 2: In tất cả vé...");
                inVe(maHoaDon);
                
                System.out.println("✅ Hoàn thành tự động in hóa đơn và vé!");
                
            } catch (Exception e) {
                System.err.println("❌ Lỗi khi tự động in: " + e.getMessage());
                e.printStackTrace();
            }
        }).start();
    }
    
    /**
     * In hóa đơn
     */
    private void inHoaDon(String maHoaDon) {
        try {
            System.out.println("🖨️ In hóa đơn: " + maHoaDon);
            
            // Load hóa đơn từ RMI service
            IHoaDonService hoaDonService = ClientContext.getHoaDonService();
            HoaDon hoaDon = hoaDonService.findByMaHoaDon(maHoaDon);
            
            if (hoaDon == null) {
                System.err.println("❌ Không tìm thấy hóa đơn: " + maHoaDon);
                return;
            }
            
            // Load chi tiết hóa đơn qua RMI
            List<ChiTietHoaDon> chiTietList = hoaDonService.getChiTietByMaHoaDon(maHoaDon);
            
            // Set danh sách chi tiết vào hóa đơn (Hibernate JPA tự động nạp các vé liên quan trong chi tiết)
            hoaDon.setDanhSachChiTiet(chiTietList);
            
            // In hóa đơn (chế độ tự động)
            ThermalPrinter printer = new ThermalPrinter(hoaDon, chiTietList);
            printer.setAutoPrint(true); // ⚡ Đánh dấu là in tự động sau bán
            boolean success = printer.printInvoice();
            
            if (!success) {
                System.err.println("❌ Lỗi khi in hóa đơn!");
            } else {
                System.out.println("✅ In hóa đơn thành công!");
            }
        } catch (Exception e) {
            System.err.println("❌ Lỗi khi in hóa đơn qua RMI: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    /**
     * In từng vé (mỗi vé cách nhau 2 giây)
     */
    private void inVe(String maHoaDon) {
        try {
            System.out.println("🖨️ In vé từ hóa đơn: " + maHoaDon);
            
            // Load danh sách vé từ RMI service
            IVeService veService = ClientContext.getVeService();
            List<Ve> danhSachVe = veService.findByMaHoaDon(maHoaDon);
            
            if (danhSachVe == null || danhSachVe.isEmpty()) {
                System.err.println("❌ Không tìm thấy vé nào cho hóa đơn: " + maHoaDon);
                return;
            }
            
            System.out.println("📄 Bắt đầu in " + danhSachVe.size() + " vé...");
            
            // In từng vé, mỗi vé cách nhau 2 giây
            for (int i = 0; i < danhSachVe.size(); i++) {
                Ve ve = danhSachVe.get(i);
                
                System.out.println("🖨️ In vé " + (i + 1) + "/" + danhSachVe.size() + ": " + ve.getMaVe());
                boolean success = ThermalPrinter.printTicket(ve, true); // ⚡ Chế độ in tự động
                
                if (!success) {
                    System.err.println("❌ Lỗi khi in vé: " + ve.getMaVe());
                } else {
                    System.out.println("✅ In vé thành công: " + ve.getMaVe());
                }
                
                // Đợi 2 giây trước khi in vé tiếp theo (trừ vé cuối cùng)
                if (i < danhSachVe.size() - 1) {
                    Thread.sleep(2000);
                }
            }
            
            System.out.println("✅ Hoàn thành in tất cả vé!");
        } catch (Exception e) {
            System.err.println("❌ Lỗi khi in vé qua RMI: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
