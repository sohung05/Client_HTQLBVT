    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {
        String tenTaiKhoan = txtTaiKhoan.getText().trim();
        // Lấy mật khẩu từ JPasswordField (trả về mảng char, cần đổi sang String)
        String matKhau = new String(txtMatKhau.getPassword());

        // 1. Kiểm tra mật khẩu rỗng riêng biệt theo yêu cầu
        if (matKhau.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền mật khẩu!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            txtMatKhau.requestFocus();
            return;
        }

        // 2. Validate tài khoản rỗng
        if (tenTaiKhoan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền tên tài khoản!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            txtTaiKhoan.requestFocus();
            return;
        }

        // 3. Skip local DB check (handled by RMI)
        
        // 4. Thực hiện đăng nhập qua RMI
        try {
            TaiKhoan tk = taiKhoanService.dangNhap(tenTaiKhoan, matKhau);

            if (tk != null) {
                // Lấy trực tiếp NhanVien từ TaiKhoan (đã được Hibernate nạp)
                NhanVien nv = tk.getNhanVien();

                if (nv != null) {
                    // --- QUAN TRỌNG: Lưu vào Session ---
                    SessionManager.getInstance().setNhanVienDangNhap(nv);

                    // Mở màn hình chính
                    openMainFrame();
                } else {
                    JOptionPane.showMessageDialog(this, "Tài khoản không liên kết với nhân viên nào!", "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Sai tên tài khoản hoặc mật khẩu!", "Đăng nhập thất bại", JOptionPane.ERROR_MESSAGE);
                txtMatKhau.requestFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi kết nối Server: " + e.getMessage());
        }
    }

    private void openMainFrame() {
        // Lấy cửa sổ Login hiện tại để đóng sau này
        Window loginWindow = SwingUtilities.getWindowAncestor(this);

        SwingUtilities.invokeLater(() -> {
            try {
                // Khởi tạo màn hình chính
                // Giả sử class Main của bạn nằm trong package demo
                iuh.fit.demo.Main mainFrame = new iuh.fit.demo.Main();
                mainFrame.setVisible(true);

                // Đóng màn hình đăng nhập
                if (loginWindow != null) {
                    loginWindow.dispose();
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi khởi tạo màn hình chính: " + e.getMessage());
            }
        });
    }

